const express = require("express");
const os = require("os");
const process = require("process");

const app = express();
const PORT = 3000;

console.log(`ğŸš« Running in single process mode`);
console.log(`ğŸ§  PID: ${process.pid}`);
console.log(`ğŸ’» Available CPUs (not used): ${os.cpus().length}`);

// ğŸ§ª Simulate a CPU-bound task like generating a large report
app.get("/generate-report", (req, res) => {
  console.log(`ğŸ“„ [/generate-report] Request received by PID ${process.pid}`);
  let total = 0;
  for (let i = 0; i < 1e5; i++) {
    for (let j = 0; j < 1e4; j++) {
      total += Math.sin(i) * Math.tan(j) * 0.0000001;
    }
  }
  res.send(`ğŸ“„ Report generated by PID ${process.pid}: ${total.toFixed(2)}`);
  console.log(`ğŸ“„ Report generated by PID ${process.pid}: ${total.toFixed(2)}`);
});

app.listen(PORT, () => {
  console.log(`ğŸš€ Server listening at http://localhost:${PORT}`);
});

