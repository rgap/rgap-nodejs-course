const express = require("express");
const os = require("os");
const process = require("process");

const app = express();
const PORT = process.env.PORT || 3000;
const pid = process.pid;
const cpuCount = os.cpus().length;

console.log(`ðŸ”§ Worker PID: ${pid} | CPUs available: ${cpuCount}`);

app.get("/generate-report", (req, res) => {
  console.log(`ðŸ“„ [/generate-report] Handled by PID ${pid}`);
  let total = 0;

  // Simulate intensive computation
  for (let i = 0; i < 1e5; i++) {
    for (let j = 0; j < 1e4; j++) {
      total += Math.sin(i) * Math.tan(j) * 0.0000001;
    }
  }

  res.send(`ðŸ“„ Report generated by PID ${pid}: ${total.toFixed(2)}`);
});

app.listen(PORT, () => {
  console.log(`ðŸš€ Server listening at http://localhost:${PORT} (PID: ${pid})`);
});
